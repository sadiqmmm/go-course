package main

import "fmt"

func main() {
	
	// Integer example
	var a int32

	a = 15

	fmt.Println("Value of integer a: ", a)	

  // Float example
	var f float32

	f = 15.2

	fmt.Println("Value of float f: ", f)

	// boolean example

	var set bool

	set = true

	fmt.Println("Value of bool set: ", set)

	// string
	
	var str string

	str = "This is string variable"

	fmt.Println("Value of string :", str)

}